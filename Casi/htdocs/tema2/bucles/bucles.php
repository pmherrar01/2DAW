
<?php
// Mostrar del 1 al 10 con while
$i = 1;
while ($i <= 10) {
    echo $i . " ";
    $i++;
}
echo "\n";
// Mostrar del 11 al 20 con do-while
$i = 11;
do {
    echo $i . " ";
    $i++;
} while ($i <= 20);
echo "\n";
// Mostrar del 21 al 30 con for
for ($i = 21; $i <= 30; $i++) {
    echo $i . " ";
}
?>